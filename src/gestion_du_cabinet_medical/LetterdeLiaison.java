void letter()
 {
try {
 DateFormat df = new SimpleDateFormat("dd-MMMM-yyyy"); 
cabinetmedical cabinet=new cabinetmedical();
String req_sql = "SELECT * FROM letterdeliaison";
ResultSet rs_medical = cabinet.statement.executeQuery(req_sql);
 String titre [] = { "Numero Letter","Date Letter", "Docteur","Numero Rendezvous", "CIN", "Patient"};
 int indis = 0; 
rs_medical.last();
String donnee_bd[][] = new String[rs_medical.getRow()][6];
rs_medical.beforeFirst();
 while (rs_medical.next()) { 
donnee_bd[indis][0] = ""+rs_medical.getString(1);
donnee_bd[indis][1] = ""+df.format(rs_medical.getDate(2));
donnee_bd[indis][2] = ""+rs_medical.getString(3);
donnee_bd[indis][3] = ""+rs_medical.getString(4);
donnee_bd[indis][4] = ""+rs_medical.getString(5);
   cabinetmedical cabinet1=new cabinetmedical();
     ResultSet rs_medical1 = cabinet1.statement.executeQuery("SELECT * FROM patient where cin='"+rs_medical.getString(5)+"'");
     if(rs_medical1.next())donnee_bd[indis][5] = ""+rs_medical1.getString("nom")+"\t"+rs_medical1.getString("prenom");
     else donnee_bd[indis][5] = ""+rs_medical.getString(5);
     rs_medical1.close();
 indis++;
}
DefaultTableModel model = new DefaultTableModel(donnee_bd, titre );
jTable1.setModel(model);  
} catch (SQLException ex) {
ex.printStackTrace();
}
}
void letter(String a)
 {
try {
 DateFormat df = new SimpleDateFormat("dd-MMMM-yyyy"); 
cabinetmedical cabinet=new cabinetmedical();
String req_sql = "SELECT * FROM letterdeliaison where patient='"+a+"'";
ResultSet rs_medical = cabinet.statement.executeQuery(req_sql);
 String titre [] = { "Numero Letter","Date Letter", "Docteur","Numero Rendezvous", "CIN", "Patient"};
 int indis = 0; 
rs_medical.last();
String donnee_bd[][] = new String[rs_medical.getRow()][6];
rs_medical.beforeFirst();
 while (rs_medical.next()) { 
     cabinetmedical cabinet1=new cabinetmedical();
     ResultSet rs_medical1 = cabinet1.statement.executeQuery("SELECT * FROM patient where cin='"+rs_medical.getString(5)+"'");
     if(rs_medical1.next())donnee_bd[indis][5] = ""+rs_medical1.getString("nom")+"\t"+rs_medical1.getString("prenom");
     else donnee_bd[indis][5] = ""+rs_medical.getString(5);
donnee_bd[indis][0] = ""+rs_medical.getString(1);
donnee_bd[indis][1] = ""+df.format(rs_medical.getDate(2));
donnee_bd[indis][2] = ""+rs_medical.getString(3);
donnee_bd[indis][3] = ""+rs_medical.getString(4); 
donnee_bd[indis][4] = ""+rs_medical.getString(5); 
 indis++;
 rs_medical1.close();
}
DefaultTableModel model = new DefaultTableModel(donnee_bd, titre );
jTable1.setModel(model);  
} catch (SQLException ex) {
ex.printStackTrace();
}
}
